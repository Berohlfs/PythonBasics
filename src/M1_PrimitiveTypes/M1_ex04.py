import math  # https://docs.python.org/3/library/math.html

# Number functions
print('\nNumber methods:')
number = -5.4
print(round(number))
print(abs(number))

# Math module methods
print('\nMath module methods:')
print(math.ceil(number))
print(math.floor(number))
print(math.factorial(abs(round(number))))
