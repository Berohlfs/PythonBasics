# Concatenation
first_name = ' BerNArDo '
last_name = 'RoHlFS'
print('\nConcatenation:' + first_name + ' ' + last_name)

# Interpolation (f-strings or formatted strings)
print(f'\nInterpolation: {first_name} {last_name}')

# String methods
print('\nString methods:')
print(first_name.upper())
print(first_name.lower())
print(first_name.title())
print(first_name.strip())
print(first_name.rstrip())
print(first_name.lstrip())
print(first_name.find('o'))
print(first_name.replace('o', '(REPLACED)'))
print('Hello' in first_name)
print('Hello' not in first_name)
